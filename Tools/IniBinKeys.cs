    /// <summary>
    /// Class used to try to reverse the hash to a full var name
    /// This class should be used only for test / reverse engeniring purpose
    /// as it take alot process / memory usage to fill KnownVarNames
    /// </summary>
    public class IniBinKeys
    {
        private static readonly Dictionary<UInt32, string> KnownVarNames = new Dictionary<UInt32, string>();

        #warning Should be used for dev only.
        public static bool ContainsKey(UInt32 hash)
        {
            if (KnownVarNames.Count == 0)
            {
                foreach (var __cat in Categories)
                {
                    foreach (var __name in VarName)
                    {
                        string __fullvarname = __cat.ToLowerInvariant() + "*" + __name.ToLowerInvariant();
                        var __hash = IniBinHash.GetHash(__fullvarname);
                        if (!KnownVarNames.ContainsKey(__hash))
                        {
                            KnownVarNames.Add(__hash, __fullvarname);
                        }
                    }
                }
            }
            return KnownVarNames.ContainsKey(hash);
        }
        /// <summary>
        /// try to reverse the hash to a full var name
        /// </summary>
        /// <param name="hash">hash entry</param>
        /// <returns>hash string or full var name if founded</returns>
        #warning Should be used for dev only.
        public static string GetKey(UInt32 hash)
        {
            return ContainsKey(hash) ? KnownVarNames[hash] : hash.ToString();
        }

